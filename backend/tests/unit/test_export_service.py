def _make_pipeline(nodes, edges):
    """Helper to create a PipelineJSON from dicts."""
    return PipelineJSON(
        version="1.0",
        nodes=[
            PipelineNode(
                id=n["id"],
                type=n["type"],
                label=n["label"],
                position=NodePosition(x=0, y=0),
                parameters=n.get("parameters", {}),
            )
            for n in nodes
        ],
        edges=[
            PipelineEdge(
                id=e["id"],
                source=e["source"],
                target=e["target"],
                sourcePort=e["sourcePort"],
                targetPort=e["targetPort"],
            )
            for e in edges
        ],
    )


