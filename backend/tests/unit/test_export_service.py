class TestExportPython:
    def test_basic_export_produces_python_code(self, export_service):
        pipeline = _classification_pipeline()
        code = export_service.export_python(pipeline, "Iris Classification")

        assert "Generated by VRL ML Studio" in code
        assert "Pipeline: Iris Classification" in code
        assert "import" in code
        assert "RandomForestClassifier" in code
        assert "classification_report" in code

    def test_export_includes_steps(self, export_service):
        pipeline = _classification_pipeline()
        code = export_service.export_python(pipeline)

        assert "# Step 1:" in code
        assert "# Step 2:" in code
        assert "# Step 3:" in code
        assert "# Step 4:" in code

    def test_export_deduplicates_imports(self, export_service):
        pipeline = _classification_pipeline()
        code = export_service.export_python(pipeline)

        lines = code.split("\n")
        import_lines = [l for l in lines if l.startswith("import ") or l.startswith("from ")]
        # No duplicate imports
        assert len(import_lines) == len(set(import_lines))

    def test_single_node_export(self, export_service):
        pipeline = _make_pipeline(
            nodes=[{
                "id": "n1",
                "type": "vrl.core.sample_dataset",
                "label": "Iris",
                "parameters": {"dataset": "iris"},
            }],
            edges=[],
        )
        code = export_service.export_python(pipeline)
        assert "load_iris" in code
        assert "Step 1: Iris" in code


class TestExportNotebook:
    def test_basic_notebook_structure(self, export_service):
        pipeline = _classification_pipeline()
        nb = export_service.export_notebook(pipeline, "Iris Classification")

        assert nb["nbformat"] == 4
        assert "cells" in nb
        assert len(nb["cells"]) >= 5  # title + pip + imports + 4 steps

    def test_notebook_has_title_cell(self, export_service):
        pipeline = _classification_pipeline()
        nb = export_service.export_notebook(pipeline, "Iris Classification")

        title_cell = nb["cells"][0]
        assert title_cell["cell_type"] == "markdown"
        assert any("Iris Classification" in line for line in title_cell["source"])

    def test_notebook_has_pip_install(self, export_service):
        pipeline = _classification_pipeline()
        nb = export_service.export_notebook(pipeline, "Test")

        # Find the pip install cell
        pip_cells = [
            c for c in nb["cells"]
            if c["cell_type"] == "code" and any("pip install" in l for l in c["source"])
        ]
        assert len(pip_cells) == 1
        pip_src = "".join(pip_cells[0]["source"])
        assert "scikit-learn" in pip_src

    def test_notebook_has_markdown_step_headers(self, export_service):
        pipeline = _classification_pipeline()
        nb = export_service.export_notebook(pipeline)

        md_cells = [c for c in nb["cells"] if c["cell_type"] == "markdown"]
        step_cells = [
            c for c in md_cells
            if any("## Step" in line for line in c["source"])
        ]
        assert len(step_cells) == 4  # 4 nodes = 4 step headers

    def test_notebook_is_valid_json(self, export_service):
        pipeline = _classification_pipeline()
        nb = export_service.export_notebook(pipeline)

        # Should serialize to valid JSON
        json_str = json.dumps(nb)
        parsed = json.loads(json_str)
        assert parsed["nbformat"] == 4


